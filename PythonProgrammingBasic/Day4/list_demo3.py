#Change the item/element in the list
myList1 = ['apple','banana','cherry']
print("Original list : ",myList1)

#changing the first element
myList1[0] = 'grapes'
print("first element at zero index is change: ",myList1)

# Read the list element using loop statement
print("Printing elements from list using for loop")
for i in myList1:
    print(i)
