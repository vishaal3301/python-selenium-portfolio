# Creation of list

#int list
my_list1 = [10,20,30,40,50]
print("int list -> ",my_list1)

#str list
my_list2 = ["apple","banana","cherry"]
print("str list -> ",my_list2)

#all kind of data type
my_list3 = ["apple",10,"banana",20,45,"peach","oranges"]
