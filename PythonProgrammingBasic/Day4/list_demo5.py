#To add elements/items in list
# append() and insert()

my_list = ["apple"]
print("Original List: ",my_list)

#appned() will add element at the end of the list
my_list.append("banana")
print("banana element is added: ",my_list)
my_list.append("kiwi")
print("kiwi element is added: ",my_list)

#insert() will add element in somewhere middle of the list.
my_list.insert(1,"orange")
print("insert() function: ",my_list)