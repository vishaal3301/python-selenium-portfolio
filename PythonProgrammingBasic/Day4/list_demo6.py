#Remove the item from the list

my_list = ["apple","banana","orange","kiwi","melon","grapes","pineapple","mango","chiku"]
print("Original List: ",my_list)

#pop
my_list.pop(0)
print("apple element is removed: ",my_list)

#del is a keyword
del my_list[1]
print("orange is deleted: ",my_list)

#clear()
my_list.clear()
print("all element present inside list are clear at once: ",my_list)

