# print 1 to 10 numbers using for loop

for i in range(10):
    print(i)        #0.......9

print("*"*1000)
for i in range(10):
    print(i)
print("*"*1000)
for j in range(1,11):
    print(j)        #1......10

print("*"*1000)
print("only even numbers using for loop")

for k in range(0,22,2):
    print(k)
print("*"*1000)
print("only odd numbers using for loop")

for l in range(1,22,2):
     print(l)
print("*"*1000)

print("1 to 10 in descending order using for loop")
for n in range(10,0,-1):
    print(n)
print("*"*1000)

print("5,10,15,20,25......100")

for m in range(5,101,5):
    print(m)
print("*"*1000)

