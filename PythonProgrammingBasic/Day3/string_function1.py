# ord() and chr() function is python.
# orf() function prints ASCII value of a alphabet.
# chr() gives the alphabet represented by given number

print(ord('A')) #65
print(ord('a')) #97
print(chr(65)) #A
print(chr(97)) #a

# max(), min() and len() function in str
print(max('abc')) #c
print(max('vishaal')) #v

print(min('abc')) #a
print(min('vishaal')) #a

print(len('vishaal')) #7

# in , not in operators
# in operator always returns True or False
# not in operator always returns True or False

my_str = 'vishaal'

print("aal" in my_str) #True
print('viaal' in my_str) #False

